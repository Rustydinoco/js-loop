// loop for 
// let i = 1 adalah 'initial'
// i <= 10 adalah 'condition'
// i++ adalah 'increment/decrement,

// increment looping for
for (let i = 1; i <=10; i++){
    console.log(i);
}

// decrement looping for
for (let i =10; i >=0; i--){
    console.log(i)
}

// or using a multiple
for (let i =10; i <=10000; i*=10){
    console.log(i)
}